import { mkdir, writeFile } from "node:fs/promises";
import { dirname, resolve } from "node:path";
import { knownDrivers } from "../src/state.js";
import { searchFrequencyResponses } from "../src/core/frequencyResponseScraper.js";

const limitArg = process.argv.find((arg) => arg.startsWith("--limit="));
const offsetArg = process.argv.find((arg) => arg.startsWith("--offset="));
const concurrencyArg = process.argv.find((arg) => arg.startsWith("--concurrency="));
const outputArg = process.argv.find((arg) => arg.startsWith("--output="));

const limit = limitArg ? Number(limitArg.split("=")[1]) : Number.POSITIVE_INFINITY;
const offset = offsetArg ? Number(offsetArg.split("=")[1]) : 0;
const concurrency = concurrencyArg ? Number(concurrencyArg.split("=")[1]) : 4;
const output = resolve(outputArg ? outputArg.split("=")[1] : "src/data/driverFrequencyResponses.js");

const candidates = knownDrivers
  .slice(offset, Number.isFinite(limit) ? offset + limit : undefined)
  .filter((driver) => driver?.id && (driver?.source || driver?.name));

let cursor = 0;
let scanned = 0;
let matchedDrivers = 0;
let parsedResponses = 0;
const results = {};
let lastLog = Date.now();

async function worker() {
  while (cursor < candidates.length) {
    const index = cursor;
    cursor += 1;
    const driver = candidates[index];
    const query = normalizeDriverQuery(driver);

    try {
      const payload = await searchFrequencyResponses(query, {
        limit: 6,
        partsExpressLimit: 2,
        loudspeakerDatabaseLimit: 2,
        webLimit: 2,
      });
      const parsed = selectBestParsedResults((payload.results || [])
        .filter((result) => result?.status === "parsed" && result?.response?.points?.length >= 5)
        .map((result) => ({
          title: result.title,
          url: result.url,
          source: result.source,
          status: result.status,
          format: result.format,
          reason: result.reason,
          pointCount: result.pointCount,
          frequencyMinHz: result.frequencyMinHz,
          frequencyMaxHz: result.frequencyMaxHz,
          magnitudeMinDb: result.magnitudeMinDb,
          magnitudeMaxDb: result.magnitudeMaxDb,
          score: result.score,
          response: result.response,
        })));

      scanned += 1;
      if (parsed.length) {
        results[driver.id] = parsed;
        matchedDrivers += 1;
        parsedResponses += parsed.length;
      }
      maybeLog(driver.name, parsed.length);
    } catch (error) {
      scanned += 1;
      maybeLog(driver.name, 0, error.message || "failed");
    }
  }
}

await Promise.all(Array.from({ length: Math.max(1, concurrency) }, () => worker()));

await mkdir(dirname(output), { recursive: true });
await writeFile(output, serializeModule({
  generatedAt: new Date().toISOString(),
  scanned,
  matchedDrivers,
  parsedResponses,
  results,
}), "utf8");

console.log(`Wrote ${parsedResponses} parsed responses for ${matchedDrivers}/${scanned} drivers to ${output}`);

function normalizeDriverQuery(driver) {
  const source = String(driver?.source || "").trim();
  if (/^https?:\/\//i.test(source)) return source;
  return String(driver?.name || source).trim();
}

function maybeLog(name, matches, error = "") {
  const now = Date.now();
  if (now - lastLog < 1200 && scanned !== candidates.length) return;
  lastLog = now;
  console.log(`scanned=${scanned}/${candidates.length} matchedDrivers=${matchedDrivers} parsedResponses=${parsedResponses} current="${name}" matches=${matches}${error ? ` error="${error}"` : ""}`);
}

function serializeModule(payload) {
  return `// Generated by scripts/import-driver-frequency-responses.mjs. Do not edit by hand.\nexport const driverFrequencyResponses = ${JSON.stringify(payload.results, null, 2)};\nexport const driverFrequencyResponseImportMeta = ${JSON.stringify({
    generatedAt: payload.generatedAt,
    scanned: payload.scanned,
    matchedDrivers: payload.matchedDrivers,
    parsedResponses: payload.parsedResponses,
  }, null, 2)};\n`;
}

function selectBestParsedResults(results) {
  return results
    .sort((left, right) => scoreParsedResult(right) - scoreParsedResult(left))
    .slice(0, 1);
}

function scoreParsedResult(result) {
  const response = result?.response || {};
  let score = Number(result?.score) || 0;
  if (Math.abs(Number(response.angleDeg) || 0) < 0.001) score += 1000;
  if (response.plane === "horizontal") score += 100;
  if (/\b0(?:deg)?\b/i.test(`${result?.title || ""} ${result?.url || ""}`)) score += 100;
  return score;
}
